// Operators in JS

// Arithmatic Operators
let a = 54;
let b = 45;

console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);

// Increment operators

console.log(a++);
console.log(++a);
console.log(a--);
console.log(--a);

// Assignment Operator
console.log(a+=9);
console.log(a-=9);
console.log(a*=9);
console.log(a/=9);
console.log(a%=9);
console.log(a**=2);

// Comparison Operator
console.log(a>b);
console.log(a<b);
console.log(a<=b);
console.log(a>=b);
console.log(a!=b);
console.log(a==b);
console.log(a===b);
console.log(a!==b);
console.log((a>b)?5:8);

// Logical Operators
console.log((a!=b)&&(a<b));
console.log((a==b)||(a<b));
console.log(!(a==b));

